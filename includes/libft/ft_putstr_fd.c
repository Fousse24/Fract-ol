#include	"libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (s == NULL)
		return ;
	if (fd >= 0 && fd <= 2)
	{
		while (*s)
			ft_putchar_fd(*s++, fd);
	}
}
