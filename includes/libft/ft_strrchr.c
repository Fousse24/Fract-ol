#include	"libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	if (!*s && c)
		return (0);
	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
